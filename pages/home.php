    <p>
        Decorative painting is the art of transforming ordinary surfaces into visually captivating works that reflect
        style, history, and personality. Through techniques such as faux finishes, glazing, marbling, stenciling, and
        gilding, artists create texture, depth, and illusion. Blending craftsmanship with creativity, decorative
        painting enhances architecture and interiors, enriching spaces with timeless elegance, warmth, and artistic
        character.
    </p>

    <p>
        Historical restoration is the meticulous art of preserving and revitalizing buildings, artworks, and
        architectural details to their original beauty and integrity. It combines research, craftsmanship, and
        conservation science to honor historical authenticity while ensuring structural stability. Through careful
        material selection and period-accurate techniques, restoration protects cultural heritage and connects the past
        to future generations.
    </p>

    <p>
        These galleries—<strong>Black & White</strong>, <strong>The Drip Series</strong>, <strong>Encaustic</strong>,
        and <strong>The Projects Series</strong>—trace an evolving artistic journey. Each explores material, mood, and
        meaning through distinct techniques: the purity of monochrome, the spontaneity of dripping paint, the luminous
        depth of encaustic wax, and the conceptual layers of ongoing projects. Together, they reveal experimentation,
        emotion, and transformation.
    </p>
