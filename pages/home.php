   <p>
       <a href="/decorative?showSlideshow=true" class="dropcap" data-gallery="decorative"
           aria-label="Go to Decorative Painting">D</a>
       ecorative painting is the art of transforming ordinary surfaces into visually captivating works that reflect
       style, history, and personality. Through techniques such as faux finishes, glazing, marbling, stenciling, and
       gilding, artists create texture, depth, and illusion. Blending craftsmanship with creativity, decorative painting
       enhances architecture and interiors, enriching spaces with timeless elegance, warmth, and artistic character
   <p>
       <a href="/restoration?showSlideshow=true" class="dropcap" data-gallery="restoration"
           aria-label="Go to Restoration Projects">H</a>
       istorical restoration is the meticulous art of preserving and revitalizing buildings, artworks, and architectural
       details to their original beauty and integrity. It combines research, craftsmanship, and conservation science to
       honor historical authenticity while ensuring structural stability. Through careful material selection and
       period-accurate techniques, restoration protects cultural heritage and connects the past to future generations.
   </p>

